package ru.practicum.config;

/**
 * Перечисление типов топиков Kafka, используемых в системе телеметрии.
 * Определяет категории топиков для различных типов событий умного дома.
 * Каждый топик соответствует определенному виду данных и имеет свою схему обработки.
 */
public enum TopicType {

    /**
     * Топик для событий от датчиков умного дома.
     * Содержит данные о показаниях различных сенсоров: движения, температуры, освещенности и др.
     * Используется для реального мониторинга состояния устройств.
     */
    TELEMETRY_SENSORS,

    /**
     * Топик для снапшотов состояния системы.
     * Содержит агрегированные данные о состоянии всех устройств в определенный момент времени.
     * Используется для аналитики и восстановления состояния системы.
     */
    TELEMETRY_SNAPSHOTS,

    /**
     * Топик для событий управления хабами.
     * Содержит команды и события, связанные с работой хабов: добавление/удаление устройств, сценарии.
     * Используется для управления конфигурацией системы.
     */
    TELEMETRY_HUBS
}
