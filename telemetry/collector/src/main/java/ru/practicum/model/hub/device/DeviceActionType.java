package ru.practicum.model.hub.device;

/**
 * Перечисление типов действий, которые могут выполняться устройствами в сценариях автоматизации.
 * Определяет операции, применяемые к устройствам при срабатывании условий сценария.
 */
public enum DeviceActionType {

    /**
     * Активация устройства - включение или приведение в активное состояние.
     */
    ACTIVATE,

    /**
     * Деактивация устройства - выключение или приведение в неактивное состояние.
     */
    DEACTIVATE,

    /**
     * Инверсия текущего состояния устройства.
     * Если устройство было включено - выключается, если выключено - включается.
     * Например: переключение состояния переключателя.
     */
    INVERSE,

    /**
     * Установка конкретного значения для устройства.
     * Требует дополнительного параметра value для указания целевого значения.
     * Например: установка конкретной температуры на термостате, уровня яркости света.
     */
    SET_VALUE
}
