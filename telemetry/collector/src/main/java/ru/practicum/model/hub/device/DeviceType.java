package ru.practicum.model.hub.device;

/**
 * Перечисление типов устройств, поддерживаемых системой умного дома.
 * Определяет категории устройств и их функциональные возможности.
 */
public enum DeviceType {

    /**
     * Датчик движения - обнаруживает перемещение в зоне покрытия.
     */
    MOTION_SENSOR,

    /**
     * Датчик температуры - измеряет температуру окружающей среды.
     */
    TEMPERATURE_SENSOR,

    /**
     * Датчик освещенности - измеряет уровень естественного и искусственного освещения.
     */
    LIGHT_SENSOR,

    /**
     * Климатический датчик - комплексно измеряет параметры микроклимата.
     * Включает температуру, влажность, уровень CO2.
     */
    CLIMATE_SENSOR,

    /**
     * Датчик-переключатель - управляет состоянием включено/выключено.
     * Используется для управления устройствами и отслеживания их состояния.
     */
    SWITCH_SENSOR
}
