package ru.practicum.model.hub.scenario;

/**
 * Перечисление типов условий, которые могут использоваться в сценариях автоматизации.
 * Определяет какие параметры датчиков могут проверяться в условиях сценариев.
 */
public enum ScenarioType {

    /**
     * Условие на основе обнаружения движения.
     * Проверяет наличие или отсутствие движения в зоне датчика.
     */
    MOTION,

    /**
     * Условие на основе уровня освещенности.
     * Проверяет яркость освещения в люксах.
     */
    LUMINOSITY,

    /**
     * Условие на основе состояния переключателя.
     * Проверяет включено или выключено устройство.
     */
    SWITCH,

    /**
     * Условие на основе температуры.
     * Проверяет температурные показатели в градусах Цельсия или Фаренгейта.
     */
    TEMPERATURE,

    /**
     * Условие на основе уровня углекислого газа.
     * Проверяет концентрацию CO2 в воздухе.
     */
    CO2LEVEL,

    /**
     * Условие на основе влажности воздуха.
     * Проверяет относительную влажность в процентах.
     */
    HUMIDITY
}
