package ru.practicum.model.sensor;

/**
 * Перечисление типов событий, генерируемых датчиками умного дома.
 * Определяет категории событий телеметрии и показаний сенсоров.
 */
public enum SensorEventType {

    /**
     * Событие от датчика движения.
     * Содержит информацию о наличии движения, качестве связи и напряжении.
     */
    MOTION_SENSOR_EVENT,

    /**
     * Событие от датчика температуры.
     * Содержит показания температуры в градусах Цельсия и Фаренгейта.
     */
    TEMPERATURE_SENSOR_EVENT,

    /**
     * Событие от датчика освещенности.
     * Содержит уровень освещенности и качество связи.
     */
    LIGHT_SENSOR_EVENT,

    /**
     * Событие от климатического датчика.
     * Содержит комплексные данные о температуре, влажности и уровне CO2.
     */
    CLIMATE_SENSOR_EVENT,

    /**
     * Событие от датчика-переключателя.
     * Содержит текущее состояние переключателя (включен/выключен).
     */
    SWITCH_SENSOR_EVENT,

    /**
     * Неизвестный тип события датчика. Используется по умолчанию при получении неподдерживаемого типа.
     * Обеспечивает обратную совместимость с будущими типами датчиков.
     */
    UNKNOWN
}
