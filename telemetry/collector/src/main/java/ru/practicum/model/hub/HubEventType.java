package ru.practicum.model.hub;

/**
 * Перечисление типов событий, генерируемых хабами умного дома.
 * Определяет категории событий управления конфигурацией системы.
 */
public enum HubEventType {

    /**
     * Событие добавления нового устройства в систему.
     * Содержит информацию о типе и идентификаторе добавленного устройства.
     */
    DEVICE_ADDED,

    /**
     * Событие удаления устройства из системы.
     * Содержит идентификатор удаляемого устройства.
     */
    DEVICE_REMOVED,

    /**
     * Событие добавления нового сценария автоматизации.
     * Содержит название, условия и действия сценария.
     */
    SCENARIO_ADDED,

    /**
     * Событие удаления сценария автоматизации.
     * Содержит название удаляемого сценария.
     */
    SCENARIO_REMOVED,

    /**
     * Неизвестный тип события. Используется по умолчанию при получении неподдерживаемого типа.
     * Позволяет обрабатывать события от будущих версий системы без ошибок.
     */
    UNKNOWN
}
