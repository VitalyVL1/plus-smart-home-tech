package ru.practicum.model.hub.scenario;

/**
 * Перечисление операций сравнения, используемых в условиях сценариев.
 * Определяет логические операции для проверки условий активации сценариев.
 */
public enum ScenarioOperation {

    /**
     * Проверка на равенство значения датчика целевому значению.
     * Используется для точного совпадения показаний.
     */
    EQUALS,

    /**
     * Проверка, что значение датчика больше целевого значения.
     * Используется для пороговых условий (температура выше заданной).
     */
    GREATER_THAN,

    /**
     * Проверка, что значение датчика меньше целевого значения.
     * Используется для пороговых условий (освещенность ниже заданной).
     */
    LOWER_THAN
}
