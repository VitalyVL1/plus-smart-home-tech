package ru.practicum.dal.model;

/**
 * Типы действий, которые могут выполняться сенсорами.
 */
public enum ActionType {
    /**
     * Активировать/включить сенсор или устройство.
     */
    ACTIVATE,

    /**
     * Деактивировать/выключить сенсор или устройство.
     */
    DEACTIVATE,

    /**
     * Инвертировать текущее состояние (вкл/выкл).
     */
    INVERSE,

    /**
     * Установить конкретное значение (яркость, температура и т.д.).
     */
    SET_VALUE
}
