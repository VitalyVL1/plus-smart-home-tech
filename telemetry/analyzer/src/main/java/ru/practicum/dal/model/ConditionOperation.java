package ru.practicum.dal.model;

/**
 * Операции сравнения для условий сценариев.
 */
public enum ConditionOperation {
    /**
     * Равно указанному значению.
     */
    EQUALS,

    /**
     * Больше указанного значения.
     */
    GREATER_THAN,

    /**
     * Меньше указанного значения.
     */
    LOWER_THAN
}
